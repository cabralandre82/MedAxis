DECLARE
  v_src text;
BEGIN
  SELECT prosrc INTO v_src
    FROM pg_proc p
    JOIN pg_namespace n ON n.oid = p.pronamespace
   WHERE n.nspname = 'public'
     AND p.proname = 'create_order_atomic';

  IF v_src IS NULL THEN
    RAISE EXCEPTION 'Migration 066 smoke: create_order_atomic missing';
  END IF;

  IF position('initial_status' IN v_src) = 0 THEN
    RAISE EXCEPTION 'Migration 066 smoke: initial_status not in body';
  END IF;

  IF position('invalid_initial_status' IN v_src) = 0 THEN
    RAISE EXCEPTION 'Migration 066 smoke: validation guard missing';
  END IF;

  RAISE NOTICE 'Migration 066 smoke passed';
END
